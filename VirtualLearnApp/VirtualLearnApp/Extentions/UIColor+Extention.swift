import Foundation
import UIKit

extension UIColor {
    
    
    //rgba(238,92,77,1)
    public class var customOrange: UIColor {
        return UIColor(red: 238/255, green: 92/255, blue: 77/255, alpha: 1.0)
    }
    
    //rgba(122,122,122,1)
    public class var customGrey: UIColor {
        return UIColor(red: 122/255, green: 122/255, blue: 122/255, alpha: 1.0)
    }
    
    //rgba(55,55,55,1)
    public class var hamburgerGrey: UIColor {
        return UIColor(red: 55/255, green: 55/255, blue: 55/255, alpha: 1.0)
    }
    
    //rgba(234,38,38,1)
    public class var hamburgerOrange: UIColor {
        return UIColor(red: 234/255, green: 38/255, blue: 38/255, alpha: 1.0)
    }
}
